// 기본적인 연산하기
console.log(23 + 45);
console.log(23 - 45);
console.log(23 * 45);
console.log(23 / 45);
console.log(23 % 45);
console.log(`52 + 45 = ${52 + 45}`);

// 문자 출력하기
console.log("안녕하세요, 희재님");
console.log("안녕하세요, '희재님'");
console.log("안녕하세요, \n희재님!");
console.log("안녕하세요, \t희재님!");
console.log("안녕하세요, \\희재님!");
console.log("안녕하세요" + "희재님!");

// 비교연산자 활용하기
console.log(54 == 54);
console.log(54 != 54);
console.log(54 > 50);
console.log(54 < 50);
console.log(54 > 50 || 5 > 10);
console.log(54 > 50 && 5 > 10);
